package com.enation.core.model;

import java.io.Serializable;

public class Grial implements Serializable{
	private int id;
	private float pop;
	private float eff;
	private float wei;
	private float gbs;
	private float srt;
	private float meta;
	private float lon;
	private long time;
	private String p1;
	private String p2;
	private String p3;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public float getPop() {
		return pop;
	}
	public void setPop(float pop) {
		this.pop = pop;
	}
	public float getEff() {
		return eff;
	}
	public void setEff(float eff) {
		this.eff = eff;
	}
	public float getWei() {
		return wei;
	}
	public void setWei(float wei) {
		this.wei = wei;
	}
	public float getGbs() {
		return gbs;
	}
	public void setGbs(float gbs) {
		this.gbs = gbs;
	}
	public float getSrt() {
		return srt;
	}
	public void setSrt(float srt) {
		this.srt = srt;
	}
	public float getMeta() {
		return meta;
	}
	public void setMeta(float meta) {
		this.meta = meta;
	}
	public float getLon() {
		return lon;
	}
	public void setLon(float lon) {
		this.lon = lon;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public String getP1() {
		return p1;
	}
	public void setP1(String p1) {
		this.p1 = p1;
	}
	public String getP2() {
		return p2;
	}
	public void setP2(String p2) {
		this.p2 = p2;
	}
	public String getP3() {
		return p3;
	}
	public void setP3(String p3) {
		this.p3 = p3;
	}
	

}
